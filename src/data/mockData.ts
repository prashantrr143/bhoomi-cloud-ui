// Generate metric data points for charts
function generateMetricDataPoints(hours: number, baseValue: number, variance: number): { timestamp: string; value: number }[] {
  const datapoints: { timestamp: string; value: number }[] = [];
  const now = new Date();
  for (let i = hours; i >= 0; i--) {
    const timestamp = new Date(now.getTime() - i * 60 * 60 * 1000);
    const value = baseValue + (Math.random() - 0.5) * variance * 2;
    datapoints.push({
      timestamp: timestamp.toISOString(),
      value: Math.max(0, Math.round(value * 100) / 100),
    });
  }
  return datapoints;
}

